"""
Code improvement implementation
"""

class ImprovedClass:
    """Improved class"""
    
    def __init__(self):
        self.data = []
    
    def improved_method(self):
        """Improved method"""
        return sorted(self.data)
    
    def optimized_operation(self):
        """Optimized operation"""
        return sum(self.data)

if __name__ == "__main__":
    obj = ImprovedClass()
    obj.improved_method()
